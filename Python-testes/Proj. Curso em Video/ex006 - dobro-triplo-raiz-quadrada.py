a = float(input('Digite um número: '))
b = a*2 #dobro
c = a*3 #triplo
d = a**(1/2) # raiz quadrada - elevar a 1/2 é igual a raiz quadrada
print('O número digitado foi: {}\nO dobro é: {}\nO triplo é: {}\nA raiz quadrada é: {:.3f}\n'.format(a,b,c,d))
# O {:.3f} é o comando de quantas casa depois da vírgula será mostrada.
